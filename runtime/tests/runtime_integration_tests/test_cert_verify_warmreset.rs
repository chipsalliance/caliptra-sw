fn build_ecdsa_verify_payload_bytes() -> Vec<u8> {
    // 1) Message + hash
    let msg = b"ECDSA384 warm reset test message";
    let hash = sha384(msg);

    // 2) Generate P-384 keypair
    let group = EcGroup::from_curve_name(Nid::SECP384R1).unwrap();
    let key = EcKey::generate(&group).unwrap();

    // 3) Sign the hash
    let sig = EcdsaSig::sign(&hash, &key).unwrap();
    let r = sig.r().to_vec();
    let s = sig.s().to_vec();

    // 4) Extract affine X/Y from pubkey
    let mut ctx = BigNumContext::new().unwrap();
    let mut x = BigNum::new().unwrap();
    let mut y = BigNum::new().unwrap();
    let pub_point = key.public_key();

    pub_point
        .affine_coordinates_gfp(&group, &mut x, &mut y, &mut ctx)
        .unwrap();

    let x_bytes = x.to_vec();
    let y_bytes = y.to_vec();

    // Helper to left-pad to 48 bytes
    fn to_48(bytes: &[u8]) -> [u8; 48] {
        let mut out = [0u8; 48];
        debug_assert!(bytes.len() <= 48);
        out[(48 - bytes.len())..].copy_from_slice(bytes);
        out
    }

    let pub_key_x = to_48(&x_bytes);
    let pub_key_y = to_48(&y_bytes);
    let signature_r = to_48(&r);
    let signature_s = to_48(&s);

    // 5) Build mailbox request as before
    let mut req = MailboxReq::EcdsaVerify(EcdsaVerifyReq {
        hdr: MailboxReqHeader { chksum: 0 },
        pub_key_x,
        pub_key_y,
        signature_r,
        signature_s,
        hash,
    });

    req.populate_chksum().unwrap();
    req.as_bytes().unwrap().to_vec()
}

/// Helper to execute the command and return (raw response bytes, parsed header).
fn send_ecdsa_verify_and_get_hdr(model: &mut DefaultHwModel, req_bytes: &[u8]) {
    let resp = model
        .mailbox_execute(u32::from(CommandId::ECDSA384_SIGNATURE_VERIFY), req_bytes)
        .unwrap()
        .expect("We should have received a response");

    let resp_hdr: &MailboxRespHeader = MailboxRespHeader::ref_from_bytes(resp.as_bytes()).unwrap();

    assert_eq!(
        resp_hdr.fips_status,
        MailboxRespHeader::FIPS_STATUS_APPROVED
    );

    // checksum (over everything after the chksum field)
    assert!(verify_checksum(
        resp_hdr.chksum,
        0x0,
        &resp[core::mem::size_of_val(&resp_hdr.chksum)..],
    ));
}

#[test]
fn test_ecdsa384_signature_verify_after_warm_reset() {
    let mut model = run_rt_test_pqc(RuntimeTestArgs::test_productions_args(), Default::default());

    // Build request once; reuse same bytes after warm reset.
    let req_bytes = build_ecdsa_verify_payload_bytes();

    // --- Before warm reset ---
    send_ecdsa_verify_and_get_hdr(&mut model, &req_bytes);

    // Warm reset
    model.warm_reset_flow().unwrap();

    // --- After warm reset (same exact request bytes) ---
    send_ecdsa_verify_and_get_hdr(&mut model, &req_bytes);
}

