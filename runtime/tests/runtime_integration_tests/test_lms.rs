fn execute_lms_cmd<T: HwModel>(
    model: &mut T,
    message: &[u8],
    pub_key_bytes: &[u8],
    signature_bytes: &[u8],
) -> Result<(), ModelError> {
    let pub_key = <LmsPublicKey<LMS_N>>::read_from(pub_key_bytes).unwrap();
    let signature = <LmsSignature<LMS_N, LMS_P, LMS_H>>::read_from(signature_bytes).unwrap();

    let mut cmd = MailboxReq::LmsVerify(LmsVerifyReq {
        hdr: MailboxReqHeader { chksum: 0 },
        pub_key_tree_type: u32::from(pub_key.tree_type.0),
        pub_key_ots_type: u32::from(pub_key.otstype.0),
        pub_key_id: pub_key.id,
        pub_key_digest: (*(pub_key.digest.as_bytes())).try_into().unwrap(),
        signature_q: u32::from(signature.q),
        signature_ots: (*(signature.ots.as_bytes())).try_into().unwrap(),
        signature_tree_type: u32::from(signature.tree_type.0),
        signature_tree_path: (*(signature.tree_path.as_bytes())).try_into().unwrap(),
    });

    cmd.populate_chksum().unwrap();

    // Stream message to SHA ACC
    model
        .compute_sha512_acc_digest(message, ShaAccMode::Sha384Stream)
        .unwrap();

    // Send LMS verify command
    let resp = model
        .mailbox_execute(u32::from(CommandId::LMS_VERIFY), cmd.as_bytes().unwrap())?
        .expect("We should have received a response");

    let resp_hdr: &MailboxRespHeader =
        LayoutVerified::<&[u8], MailboxRespHeader>::new(resp.as_bytes())
            .unwrap()
            .into_ref();

    assert_eq!(
        resp_hdr.fips_status,
        MailboxRespHeader::FIPS_STATUS_APPROVED
    );
    // Checksum is just going to be 0 because FIPS_STATUS_APPROVED is 0
    assert_eq!(resp_hdr.chksum, 0);
    assert_eq!(model.soc_ifc().cptra_fw_error_non_fatal().read(), 0);

    Ok(())
}

#[test]
fn test_lms_verify_cmd() {
    let mut model = run_rt_test(None, None, None);

    model.step_until(|m| {
        m.soc_ifc().cptra_boot_status().read() == u32::from(RtBootStatus::RtReadyForCommands)
    });

    // Message 1, key 1
    execute_lms_cmd(&mut model, &MSG_1, &MSG_1_PUB_KEY_1, &MSG_1_KEY_1_SIG_1).unwrap();
    execute_lms_cmd(&mut model, &MSG_1, &MSG_1_PUB_KEY_1, &MSG_1_KEY_1_SIG_2).unwrap();

    // Message 1, key 2
    execute_lms_cmd(&mut model, &MSG_1, &MSG_1_PUB_KEY_2, &MSG_1_KEY_2_SIG_1).unwrap();
    execute_lms_cmd(&mut model, &MSG_1, &MSG_1_PUB_KEY_2, &MSG_1_KEY_2_SIG_2).unwrap();

    // Message 2, key 1
    execute_lms_cmd(&mut model, &MSG_2, &MSG_2_PUB_KEY_1, &MSG_2_KEY_1_SIG_1).unwrap();
    execute_lms_cmd(&mut model, &MSG_2, &MSG_2_PUB_KEY_1, &MSG_2_KEY_1_SIG_2).unwrap();

    // Message 2, key 2
    execute_lms_cmd(&mut model, &MSG_2, &MSG_2_PUB_KEY_2, &MSG_2_KEY_2_SIG_1).unwrap();
    execute_lms_cmd(&mut model, &MSG_2, &MSG_2_PUB_KEY_2, &MSG_2_KEY_2_SIG_2).unwrap();
}

