fn test_kdf(
    key_0: &[u8; 48],
    msg_0: &[u8],
    label: &[u8],
    context: Option<&[u8]>,
    msg_1: &[u8],
    out_key_id: KeyId,
    out_value: &[u8; 48],
) {
    let mut hmac384 = unsafe { Hmac384::new(HmacReg::new()) };
    let mut trng = unsafe {
        Trng::new(
            CsrngReg::new(),
            EntropySrcReg::new(),
            SocIfcTrngReg::new(),
            &SocIfcReg::new(),
        )
        .unwrap()
    };

    let key_0 = Array4x12::from(key_0);
    let kdf_key_out = KeyWriteArgs::new(
        KeyId::KeyId0,
        KeyUsage::default().set_hmac_key_en().set_hmac_data_en(),
    );
    let kdf_key_in = KeyReadArgs::new(KeyId::KeyId0);

    hmac384
        .hmac(
            &(&key_0).into(),
            &(&msg_0.into()),
            &mut trng,
            kdf_key_out.into(),
        )
        .unwrap();

    let kdf_out = KeyWriteArgs::new(out_key_id, KeyUsage::default().set_hmac_key_en());

    hmac384_kdf(
        &mut hmac384,
        kdf_key_in.into(),
        label,
        context,
        &mut trng,
        kdf_out.into(),
    )
    .unwrap();

    let mut out_tag = Array4x12::default();
    let hmac_key = KeyReadArgs::new(out_key_id);
    hmac384
        .hmac(
            &hmac_key.into(),
            &(&msg_1.into()),
            &mut trng,
            (&mut out_tag).into(),
        )
        .unwrap();
    assert_eq!(out_tag, Array4x12::from(out_value));
}

